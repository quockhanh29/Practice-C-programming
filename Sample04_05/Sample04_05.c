#include<stdio.h>

int main(void){

	int number1 = 23;
	int number2 = 10;

	printf("算術演算子の練習です\n");
	printf("%d + %d = %d\n", number1, number2, number1 + number2);
	printf("%d - %d = %d\n", number1, number2, number1 - number2);
	printf("%d * %d = %d\n", number1, number2, number1 * number2);
	printf("%d / %d = %d\n", number1, number2, number1 / number2);
	printf("%d %% %d = %d\n", number1, number2, number1 % number2);

	return 0;

}

